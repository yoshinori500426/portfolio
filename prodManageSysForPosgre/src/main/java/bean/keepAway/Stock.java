package bean;

public class Stock {
	private String productNo;
	private String deliveryDate;
	private String customerNo;
	private String customerName;
	private String poNo;
	private int poQty;
	private String supprierNo;
	private String supprierName;
	private int orQty;
	private String finFlg;

	public String getProductNo() {
		return productNo;
	}
	public void setProductNo(String productNo) {
		this.productNo = productNo;
	}
	public String getDeliveryDate() {
		return deliveryDate;
	}
	public void setDeliveryDate(String deliveryDate) {
		this.deliveryDate = deliveryDate;
	}
	public String getCustomerNo() {
		return customerNo;
	}
	public void setCustomerNo(String customerNo) {
		this.customerNo = customerNo;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getPoNo() {
		return poNo;
	}
	public void setPoNo(String poNo) {
		this.poNo = poNo;
	}
	public int getPoQty() {
		return poQty;
	}
	public void setPoQty(int poQty) {
		this.poQty = poQty;
	}
	public String getSupprierNo() {
		return supprierNo;
	}
	public void setSupprierNo(String supprierNo) {
		this.supprierNo = supprierNo;
	}
	public String getSupprierName() {
		return supprierName;
	}
	public void setSupprierName(String supprierName) {
		this.supprierName = supprierName;
	}
	public int getOrQty() {
		return orQty;
	}
	public void setOrQty(int orQty) {
		this.orQty = orQty;
	}
	public String getFinFlg() {
		return finFlg;
	}
	public void setFinFlg(String finFlg) {
		this.finFlg = finFlg;
	}
}
